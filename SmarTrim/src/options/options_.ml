let kind_setter (kind : string) =
  let kind' = String.lowercase_ascii kind in
  match kind' with
  | "all" ->
    (* set all but reg checking *)
    Chk.io := true;
    Chk.dz := true;
    Chk.assert_ := true;
    Chk.el := true;
    Chk.su := false;
    Chk.erc20 := true;
    Chk.re := false
  | "io" -> Chk.io := true
  | "dz" -> Chk.dz := true
  | "as" | "assert" -> Chk.assert_ := true
  | "el" | "leak" -> Chk.el := true
  | "su" | "ka" | "kill" -> Chk.su := true
  | "re" -> Chk.re := true
  | "tx" -> Chk.tx := true
  | "erc20" -> Chk.erc20 := true
  | "reg" -> Chk.reg := true
  | _ -> invalid_arg ("invalid option in specifying bug types: " ^ kind)
;;

