let activate_default_detector_if_unspecified () =
  let b = Chk.(!io || !dz || !assert_ || !el || !su || !erc20 || !re || !tx || !reg) in
  if not b then begin
    let open Chk in
    io := true;
    dz := true;
    el := true;
    su := true
  end;
  if !R.overify || !R.pverify then Chk.reg := true
;;

let print_detector_activation_status () =
  let open Chk in
  if !verbose > 0 then begin
    if !io then print_endline "[CHECKER] Integer Over/Underflows";
    if !dz then print_endline "[CHECKER] Division by Zero";
    if !assert_ then print_endline "[CHECKER] Assertion Violation";
    if !el then print_endline "[CHECKER] Ether Leak";
    if !su then print_endline "[CHECKER] Suicidal";
    if !erc20 then print_endline "[CHECKER] ERC20 Violation";
    if !re then print_endline "[CHECKER] Reentrancy";
    if !tx then print_endline "[CHECKER] Improper use of tx.origin";
    print_endline ""
  end
;;

